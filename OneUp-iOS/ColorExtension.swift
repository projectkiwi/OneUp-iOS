import Foundation
import UIKit

extension UIColor {
    public class func oneUp_goldColor() -> UIColor {
        return UIColor(red: 248, green: 231, blue: 28, alpha: 1)
    }
    
    public class func oneUp_silverColor() -> UIColor {
        return UIColor(red: 230, green: 230, blue: 230, alpha: 1)
    }
    // 124 89 97
    
    public class func oneUp_purpleColor() -> UIColor {
        return UIColor(red: 124, green: 89, blue: 97, alpha: 1)
    }
    
}